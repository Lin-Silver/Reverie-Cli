function interpolate(value: string, values: TranslationValues): string {
  return Object.entries(values).reduce(
    (result, [key, replacement]) => result.replaceAll(`{${key}}`, String(replacement)),
    value,
  );
}

export function normalizeUiLanguage(value: unknown): UiLanguage {
  return value === "en-US" ? "en-US" : "zh-CN";
}

export function translate(language: UiLanguage, key: string, values: TranslationValues = {}): string {
  const source = language === "en-US"
    ? ENGLISH_TEMPLATES[key] ?? ENGLISH_TRANSLATIONS[key] ?? key
    : CHINESE_TEMPLATES[key] ?? key;
  return interpolate(source, values);
}

type I18nContextValue = {
  language: UiLanguage;
  t: (key: string, values?: TranslationValues) => string;
};

const I18nContext = createContext<I18nContextValue>({
  language: "zh-CN",
  t: (key, values) => translate("zh-CN", key, values),
});

export function I18nProvider({ language, children }: { language: UiLanguage; children: ReactNode }) {
  const value = useMemo<I18nContextValue>(() => ({
    language,
    t: (key, values) => translate(language, key, values),
  }), [language]);
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nContextValue {
  return useContext(I18nContext);
}
